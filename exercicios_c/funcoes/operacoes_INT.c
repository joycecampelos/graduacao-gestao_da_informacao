#include <stdio.h>

int main()
{
    int x = 10;
    int y = 5;
    int soma = x + y;
    int multiplicacao = x * y;
    int subtracao = x - y;
    int divisao = x / y;

    printf("Resultado da soma e' %i\n", soma);
    printf("Resultado da multiplicacao e' %i\n", multiplicacao);
    printf("Resultado da subtracao e' %i\n", subtracao);
    printf("Resultado da divisao e' %i\n", divisao);

    return 0;
}
